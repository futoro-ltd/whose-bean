const ISO3_COUNTRY_NAME: Record<string, string> = Object.fromEntries(
  Object.entries(COUNTRY_3CHAR_CODE).map(([name, code]) => [code, name])
);

export function getCountryISO(countryName: string): string {
  if (!countryName) return '';
  return COUNTRY_3CHAR_CODE[countryName];
}

export function getCountryNameByISO3(iso3: string): string {
  if (!iso3) return '';
  return ISO3_COUNTRY_NAME[iso3.toUpperCase()] || '';
}

export function getCountryNameByISO2(iso2: string): string {
  if (!iso2) return '';
  return ISO2_COUNTRY_NAME[iso2.toUpperCase()] || '';
}

export function getCountryISO3Code(country: string): string {
  if (!country) return '';
  const upper = country.toUpperCase();

  if (COUNTRY_3CHAR_CODE[country]) {
    return COUNTRY_3CHAR_CODE[country];
  }

  const iso2Upper = upper.length === 2 ? upper : upper.slice(0, 2);
  const countryName = ISO2_COUNTRY_NAME[iso2Upper];
  if (countryName && COUNTRY_3CHAR_CODE[countryName]) {
    return COUNTRY_3CHAR_CODE[countryName];
  }

  if (Object.values(COUNTRY_3CHAR_CODE).includes(upper)) {
    return upper;
  }

  return '';
}
